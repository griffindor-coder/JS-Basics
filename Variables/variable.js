// Function scoped
// Very flexible as let us change the data type and hence dangerous
var one = 1;

// It is similar to var but it is block scoped. A block is reprsented by { }.
// Its data type can be changed (mutable in nature)
// Use in loops
let two = 2;

// Const is block scoped and it is immutable in nature. It should be used by default.
// Recommended
const three = 3;